public class Shape {
	private int x, y;
	
	public Shape() {
		x = 0;
		y = 0;
	}

	public String toString() {
		return "도형 :: 중심좌표 (" + x + "," + y + ")";
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
